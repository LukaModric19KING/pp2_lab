x = 5 #integer
print(type(x))

y = "Five" #string
print(type(y))

z = 5.0 #float
print(type(z))

a = 5j #complex_num
print(type(a))

b = ["Five", "five", "fIvE"] #list
print(type(b))

c = ("Five", "five", "fIvE") #tuple
print(type(c))

d = {"Five", "five", "fIvE"} #set
print(type(d))

e = b"Five" #bytes
print(type(e))
